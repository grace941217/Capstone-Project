import React from 'react'
import { useNavigate } from "react-router-dom";

function Navbar() {
    const navigate = useNavigate()


    function handleLogout() {
        sessionStorage.clear()
        navigate("/login")
    }
  
    return (
      <header>
        <button onClick={handleLogout}>Logout</button>
      </header>
    );
  }

export default Navbar;